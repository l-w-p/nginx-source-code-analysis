#include <ngx_config.h>
#include <ngx_core.h>

#ifndef _NGX_QUEUE_H_INCLUDED_
#define _NGX_QUEUE_H_INCLUDED_

typedef struct ngx_queue_s ngx_queue_t;

struct ngx_queue_s {
    ngx_queue_t *prev;
    ngx_queue_t *next;
};

#define ngx_queue_init(q)                                                      \
    (q)->prev = q;                                                             \
    (q)->next = q

#define ngx_queue_empty(h)                                                     \
    (h == (h)->prev)

/***********************************************************
 * 增                                                      *
 ***********************************************************/

// 在 h 结点之后插入 x 结点
#define ngx_queue_insert_head(h, x)                                            \
    (x)->next = (h)->next;                                                     \
    (x)->next->prev = x;                                                       \
    (x)->prev = h;                                                             \
    (h)->next = x

// 在 h 结点之前插入 x 结点
#define ngx_queue_insert_tail(h, x)                                            \
    (x)->prev = (h)->prev;                                                     \
    (x)->prev->next = x;                                                       \
    (x)->next = h;                                                             \
    (h)->prev = x

#define ngx_queue_insert_after ngx_queue_insert_head

/***********************************************************
 * 删                                                      *
 ***********************************************************/

#if (NGX_DEBUG)

#define ngx_queue_remove(x)                                                    \
    (x)->next->prev = (x)->prev;                                               \
    (x)->prev->next = (x)->next;                                               \
    (x)->prev = NULL;                                                          \
    (x)->next = NULL

#else

#define ngx_queue_remove(x)                                                    \
    (x)->next->prev = (x)->prev;                                               \
    (x)->prev->next = (x)->next

#endif

/***********************************************************
 * 查                                                      *
 ***********************************************************/

// h 结点不存储数据
#define ngx_queue_head(h)                                                      \
    (h)->next

#define ngx_queue_last(h)                                                      \
    (h)->prev

#define ngx_queue_sentinel(h)                                                  \
    (h)

#define ngx_queue_next(q)                                                      \
    (q)->next

#define ngx_queue_prev(q)                                                      \
    (q)->prev

/***********************************************************
 * 改                                                      *
 ***********************************************************/

/* 队列分割
 *
 * 在 h 为哨兵结点的队列中，从 q 结点开始到队尾结点拆除并链接
 * n 为哨兵结点的空队列中，h 队列中的剩余结点组成队列 h。
 * {
 * 插入前
 * +---------------------------------------------------------------------+
 * | +-----------------------------------------------------------------+ |
 * | |   +------+             +-----+     +------+            +-----+  | |
 * | +-> | h[m] | ----------> |  h  | --> | h[1] | ---------> |  q  | -+ |
 * +---- |      | <---------- |     | <-- |      | <--------- |     | <--+
 *       +------+             +-----+     +------+            +-----+
 *                            +-----+
 *                            |  n  |
 *                            |     |
 *                            +-----+
 *
 * 插入后
 * +---------------------------------------------------------------------+
 * | +-----------------------------------------------------------------+ |
 * | |   +------+             +-----+     +------+            +-----+  | |
 * | +-> | h[m] | ----+ +---> |  h  | --> | h[1] | ----+ +--> |  q  | -+ |
 * +---- |      | <-+ | | +-- |     | <-- |      | <-+ | | +- |     | <--+
 *       +------+   | | | |   +-----+     +------+   | | | |  +-----+
 *                  | | | +--------------------------+ | | |
 *                  | | +------------------------------+ | |
 *                  | |       +-----+                    | |
 *                  | +-----> |  n  | -------------------+ |
 *                  +-------- |     | <--------------------+
 *                            +-----+
 * }
 */
#define ngx_queue_split(h, q, n)                                               \
    (n)->prev = (h)->prev;                                                     \
    (n)->prev->next = n;                                                       \
    (n)->next = q;                                                             \
    (h)->prev = (q)->prev;                                                     \
    (h)->prev->next = h;                                                       \
    (q)->prev = n;

/* 队列合并
 *
 * 将队列 n 插入到队列 h 的尾部。
 * 合并前，h 结点为队列 h 的哨兵结点，n 结点为队列 n 的哨兵结点。
 * 合并后，h 结点为合并队列的哨兵结点，n 结点为多余的哨兵结点。
 * {
 * 插入前
 * +-------------------------------------------------------+
 * | +---------------------------------------------------+ |
 * | |   +------+             +-----+         +------+   | |
 * | +-> | h[m] | ----------> |  h  | ------> | h[1] | --+ |
 * +---- |      | <---------- |     | <------ |      | <---+
 *       +------+             +-----+         +------+
 *       +------+             +-----+         +------+
 * +---> | n[m] | ----------> |  n  | ------> | n[1] | ----+
 * | +-- |      | <---------- |     | <------ |      | <-+ |
 * | |   +------+             +-----+         +------+   | |
 * | +---------------------------------------------------+ |
 * +-------------------------------------------------------+
 *
 * 插入后
 * +-------------------------------------------------------+
 * | +---------------------------------------------------+ |
 * | |   +------+             +-----+         +------+   | |
 * | +-> | h[m] | ----+ +---> |  h  | ------> | h[1] | --+ |
 * +---- |      | <-+ | | +-- |     | <------ |      | <---+
 *       +------+   | | | |   +-----+         +------+
 *                  | +-|-|---------------+
 *                  +---|-|-------------+ |
 *       +------+       | |   +-----+   | |   +------+
 * +---> | n[m] | ------+ |   |  n  |   | +-> | n[1] | ----+
 * | +-- |      | <-------+   |     |   +---- |      | <-+ |
 * | |   +------+             +-----+         +------+   | |
 * | +---------------------------------------------------+ |
 * +-------------------------------------------------------+
 * }
 */
#define ngx_queue_add(h, n)                                                    \
    (h)->prev->next = (n)->next;                                               \
    (n)->next->prev = (h)->prev;                                               \
    (h)->prev = (n)->prev;                                                     \
    (h)->prev->next = h;                                                       \
    ngx_queue_init(n); // add by lwp

/***********************************************************
 * 通过指针偏移获取结点中的数据                            *
 ***********************************************************/

#define ngx_queue_data(q, type, link)                                          \
    (type *) ((u_char *) q - offsetof(type, link))

ngx_queue_t *ngx_queue_middle(ngx_queue_t *queue);
void ngx_queue_sort(ngx_queue_t *queue,
        ngx_int_t (*cmp)(const ngx_queue_t *, const ngx_queue_t *));

#endif /* _NGX_QUEUE_H_INCLUDED_ */
